package metodos;

public class Calculadora {
	
	public static void soma(int a, int b) {
		System.out.println("A soma de "+a+" e "+b+" é "+ a+b);
	}
	public static void subtracao(int a, int b) {
		System.out.println("A subtração de "+a+" por "+b+" é "+ (a-b));
	}
	public static void multiplicacao(int a, int b) {
		System.out.println("A multiplicação de "+a+" e "+b+" é "+ a*b);
	}
	public static void divisao(int a, int b) {
		System.out.println("A divisão de "+a+" por "+b+" é "+ a/b);
	}

}
